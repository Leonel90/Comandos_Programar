--### CLASES DE PROGRQAMACION VISUAL ###

--Comando para instalar globalmente TypeScript
npm install -g typescript

--comandos typescript
tsc --init

--comando para transpilar
tsc main.ts

--comnado para transpilar automaticamente
tsc --init -w

--comando para correr automaticamente 
tsc -w
--comando para ver version instalada de TypeScript
tsc -v

